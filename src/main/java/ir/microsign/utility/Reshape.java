public class Reshape {

public static boolean mReshape,mReshapeWeb;
    public static String reshape(String Str) {
        if (!mReshape)return Str;

        if (Str == null) return null;
        return Str;
    }
    public static String reshapeWeb(String Str) {
        if (!mReshapeWeb)return Str;
        if (Str == null) return null;
        return Str;
    }
}
